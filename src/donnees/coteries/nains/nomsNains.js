import { getRandomInt } from '../../../utils/rand'

export function genNomNain(male) {
  var nom = PRENOMS_1[getRandomInt(PRENOMS_1.length)]

  // prénom
  if (male) nom = nom + PRENOMS_M_2[getRandomInt(PRENOMS_M_2.length)]
  else nom = nom + PRENOMS_F_2[getRandomInt(PRENOMS_F_2.length)]
  // nom hérité du père / mère
  if (male)
    nom =
      nom +
      ' ' +
      PRENOMS_1[getRandomInt(PRENOMS_1.length)] +
      PRENOMS_M_2[getRandomInt(PRENOMS_M_2.length)] +
      'son'
  else
    nom =
      nom +
      ' ' +
      PRENOMS_1[getRandomInt(PRENOMS_1.length)] +
      PRENOMS_F_2[getRandomInt(PRENOMS_F_2.length)] +
      'sdotr'

  // surnom (bonus)
  nom =
    nom +
    ' (' +
    NOMS_1[getRandomInt(NOMS_1.length)] +
    '-' +
    NOMS_2[getRandomInt(NOMS_2.length)] +
    ')'
  return nom
}

