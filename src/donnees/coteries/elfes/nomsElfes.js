import { getRandomInt } from '../../../utils/rand'

export function genNomElfe(male) {
  var nom =
    PRENOM_ELT1[getRandomInt(PRENOM_ELT1.length)] +
    PRENOM_CONNECTEUR[getRandomInt(PRENOM_CONNECTEUR.length)]
  if (male) nom = nom + PRENOM_M_ELT[getRandomInt(PRENOM_M_ELT.length)]
  else nom = nom + PRENOM_F_ELT[getRandomInt(PRENOM_F_ELT.length)]

  return nom + ' ' + NOM[getRandomInt(NOM.length)]
}

